CREATE UNIQUE INDEX IF NOT EXISTS uk_chef_week_confirmation_chef_week
    ON public.chef_week_confirmation (chef_id, week_start);

CREATE INDEX IF NOT EXISTS idx_chef_week_confirmation_week
    ON public.chef_week_confirmation (week_start);

CREATE TABLE IF NOT EXISTS public.notification_dispatch_log (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid NOT NULL,
    dispatch_type character varying(120) NOT NULL,
    bucket_key character varying(120) NOT NULL,
    sent_at timestamp(6) without time zone NOT NULL DEFAULT now()
);

DO $$
BEGIN
    IF to_regclass('public.notification_dispatch_log') IS NOT NULL
        AND NOT EXISTS (
            SELECT 1
            FROM pg_constraint
            WHERE conname = 'fk_notification_dispatch_log_user'
              AND conrelid = 'public.notification_dispatch_log'::regclass
        )
    THEN
        ALTER TABLE public.notification_dispatch_log
            ADD CONSTRAINT fk_notification_dispatch_log_user
            FOREIGN KEY (user_id) REFERENCES public.users(id);
    END IF;
END $$;

CREATE UNIQUE INDEX IF NOT EXISTS uk_notification_dispatch_user_type_bucket
    ON public.notification_dispatch_log (user_id, dispatch_type, bucket_key);

CREATE INDEX IF NOT EXISTS idx_notification_dispatch_user_type
    ON public.notification_dispatch_log (user_id, dispatch_type);
